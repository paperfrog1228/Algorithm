#include <stdio.h>
int _2017[110]={0,
    500,
    300,300,
    200,200,200,
    50,50,50,50,
    30,30,30,30,30,
    10,10,10,10,10,10};
int _2018[110]={0,
    512,
    256,256,
    128,128,128,128,
    64,64,64,64,64,64,64,64,
    32,32,32,32,32,32,32,32,32,32,32,32,32,32,32,32
};
int main(){
    int t,a,b,ans;
    scanf("%d",&t);
    for(int i=1;i<=t;i++){
        ans=0;
        scanf("%d %d",&a,&b);
        ans=_2017[a]+_2018[b];
        printf("%d\n",ans*10000);
    }
}